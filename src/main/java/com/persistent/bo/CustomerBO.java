package com.persistent.bo;

public class CustomerBO {
	private String cName;
	private String cAddr;
	private double pAmt;
	private double roi;
	private double time;
	private double intrAmt;
	private String newName;
	private String newAddr;
	
	public String getcName() {
		return cName;
	}
	public void setcName(String cName) {
		this.cName = cName;
	}
	public String getcAddr() {
		return cAddr;
	}
	public void setcAddr(String cAddr) {
		this.cAddr = cAddr;
	}
	public double getpAmt() {
		return pAmt;
	}
	public void setpAmt(double pAmt) {
		this.pAmt = pAmt;
	}
	public double getRoi() {
		return roi;
	}
	public void setRoi(double roi) {
		this.roi = roi;
	}
	public double getTime() {
		return time;
	}
	public void setTime(double time) {
		this.time = time;
	}
	public double getIntrAmt() {
		return intrAmt;
	}
	public void setIntrAmt(double intrAmt) {
		this.intrAmt = intrAmt;
	}
	public String getNewName() {
		return newName;
	}
	public void setNewName(String newName) {
		this.newName = newName;
	}
	public String getNewAddr() {
		return newAddr;
	}
	public void setNewAddr(String newAddr) {
		this.newAddr = newAddr;
	}
	
}
